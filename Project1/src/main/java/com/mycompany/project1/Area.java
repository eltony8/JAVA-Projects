public class Area {
    public double circleArea(float r)
    {
        return Math.PI * r * r;
    }
    public double cubeArea(double r)
    {
        return Math.pow(r, 3);
    }
    public double rectangularArea(float x , float y)
    {
        return x * y;
    }  
    
}
